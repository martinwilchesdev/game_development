int a = 3, b = 5;
int resultado = 0;

// Suma
resultado = a + b;
Console.WriteLine(resultado);

// Resta
resultado = a - b;
Console.WriteLine(resultado);

// Multiplicacion
resultado = a * b;
Console.WriteLine(resultado);

// Modulo - Resto de la division
resultado = b % a;
Console.WriteLine(resultado);

// Aumentar en 1 el valor de la variable
resultado++;
Console.WriteLine(resultado);

// Disminuir en 1 el valor de la variable
resultado--;
Console.WriteLine(resultado);