// Los bucles ejecutan un bloque de codigo de forma repetida

int condicion = 1;
while(condicion <= 5) {
    Console.WriteLine("Ciclo While: " + condicion);
    condicion++; // Se modifica el valor de la variable evaluada para que el bucle finalice en algun momento
}

// El bucle for ejecuta un bloque de codigo un numero definido de veces
for(int i = 1; i <= 10; i++) {
    Console.WriteLine("Ciclo For: " + i);
}